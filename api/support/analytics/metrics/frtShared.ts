/**
 * Макс. длина текста, для которого применяется anti-thanks.
 * Чуть больше 50 — «Ha tushunarli Rahmat» / «ha hozir chiqdi rahmat».
 */
export const ACK_MAX_LEN = 80

/** Один символ-разделитель (пробел / пунктуация). Квантификаторы — снаружи. */
const SEP = `[\\s!.…,;:«»"'\`()\\[\\]\\-—–]`

/**
 * Целое сообщение = только ack-слова и разделители.
 * Ловит: «Рахмат кутамиз», «Хоп ака.», «Ha tushunarli Rahmat»,
 * «Да, спасибо», «СПАСИБО», «ПРИНЯТО», «ишлади рахмат», «hop raxmat».
 */
/** Длинные альтернативы раньше коротких («окей» до «ок», иначе остаток «ей»). */
const byLenDesc = (a: string, b: string) => b.length - a.length || a.localeCompare(b)
const WORDS = [...ACK_WORDS].sort(byLenDesc).join('|')
const GLUED = [...GLUED_PHRASES].sort(byLenDesc).join('|')

const FULL_MESSAGE_ACK =
  `^${SEP}*(${WORDS})(${SEP}+(${WORDS}))*${SEP}*$`

/** Слитное короткое сообщение целиком. */
const GLUED_ACK =
  `^${SEP}*(${GLUED})${SEP}*$`

export const ANTI_THANKS_REGEX = `${FULL_MESSAGE_ACK}|${GLUED_ACK}`

/** Нормализованный текст сообщения для anti-thanks в SQL (~ trim + lower). */
export const ACK_TEXT_SQL = `LOWER(trim(COALESCE(text_content, '')))`
