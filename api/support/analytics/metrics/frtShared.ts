/**
 * Единый источник правды для расчётов «первого ответа» (FRT / SLA / распределение).
 *
 * КРИТИЧНО про regex и neon: тег `sql\`...\`` подставляет COOKED-строки шаблона,
 * поэтому последовательность `\s`, вписанная прямо в текст запроса, трактуется
 * как JS-эскейп и схлопывается в `s` — граница слова по пробелу ломается.
 * Раньше это ТИХО портило anti-thanks фильтр во всех витринах: вместо границы
 * по пробелу получалось «или буква s», и фильтр ловил лишь ~730 из ~1720
 * коротких «ок/рахмат/спасибо». Недоотфильтрованные подтверждения попадали в
 * session_start как «новые запросы» и завышали объём сессий, искажая FRT и SLA.
 *
 * Поэтому regex ОБЯЗАН передаваться ПАРАМЕТРОМ (значением `~ ${ANTI_THANKS_REGEX}`),
 * а не вписываться в текст шаблона — тогда `\s` доезжает до Postgres как есть.
 *
 * Все витрины времени ответа должны импортировать эти константы, чтобы «первый
 * ответ» считался одной формулой (см. types.ts, архитектурное правило).
 *
 * Метрики времени ответа (frt_avg_minutes, sla_compliance_rate, repeat_contact_rate)
 * дополнительно исключают каналы с type или sla_category = internal — внутренние
 * командные чаты не относятся к клиентскому обслуживанию.
 */

/** Окно детекции первого ответа: ответ позже — считается «нет ответа в окне». */
export const FRT_WINDOW_HOURS = 4

/**
 * Короткие подтверждения-«спасибо», которые НЕ открывают новую сессию.
 * Только для сообщений ≤ 50 символов.
 *
 * Две ветки (OR):
 * 1) SPACED — отдельное слово с границей пробела/края/пунктуации («хоп», «болди»).
 * 2) GLUED — целое короткое сообщение без пробелов («хопболади», «hopboladi»).
 *    Клиенты часто пишут «ок/спасибо» слитно после решения — это не новый запрос.
 */
const SPACED_ACK =
  '(^|\\s)(хоп|ок|окей|рахмат|спасибо|тушунарли|хорошо|понял|ладно|rahmat|ok|okay|tushunarli|hop|болди|болади|булади|buldi|майли|mayli|да|нет|йук|ха|понятно|good|thanks|thank you|hozir|тушундим|хн|aни)(\\s|$|[!.…,])'

const GLUED_ACK =
  '^[\\s!.…,«»"\'()\\-]*' +
  '(хопболади|хопболди|хопбулди|хопрахмат|хопрэхмат|hopboladi|hopboldi|hopboladi|' +
  'окболди|окболади|окспасибо|рахматкатта|каттарахмат|хопрахмет)' +
  '[\\s!.…,«»"\'()\\-]*$'

export const ANTI_THANKS_REGEX = `${SPACED_ACK}|${GLUED_ACK}`

/** Нормализованный текст сообщения для anti-thanks в SQL (~ trim + lower). */
export const ACK_TEXT_SQL = `LOWER(trim(COALESCE(text_content, '')))`
